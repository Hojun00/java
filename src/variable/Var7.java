package variable;

public class Var7 {
    public static void main(String[] args){
        int a = 100; //정수형
        double b = 10.5; //실수형
        boolean c = true; //불리언
        char d = 'A'; //char형은 문자 한개만 넣을 수 있는 변수형이다. 만약 char d = 'AB' 로 선언하면 오류가 발생한다.
        String e = "Hello Java"; // String형은 문자열을 저장하는 변수형이며, 특별한 변수이기에 앞글자에 대문자를 사용한다.

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
